import type { NextConfig } from "next";

const isDev = process.env.NODE_ENV === "development";

// CSP without nonces, so the app stays statically rendered (nonces would force
// dynamic rendering). `img-src` must allow remote journal covers (arbitrary CSV
// URLs) plus uploaded data:/blob: images. `'unsafe-inline'` is required for
// Next's inline hydration/bootstrap scripts in the non-nonce approach; harden
// further with nonces or experimental SRI if stricter XSS protection is needed.
const contentSecurityPolicy = [
  "default-src 'self'",
  `script-src 'self' 'unsafe-inline'${isDev ? " 'unsafe-eval'" : ""}`,
  "style-src 'self' 'unsafe-inline'",
  "img-src 'self' blob: data: https:",
  "font-src 'self'",
  "object-src 'none'",
  "base-uri 'self'",
  "form-action 'self'",
  "frame-ancestors 'none'",
].join("; ");

const securityHeaders = [
  { key: "Content-Security-Policy", value: contentSecurityPolicy },
  { key: "X-Frame-Options", value: "DENY" },
  { key: "X-Content-Type-Options", value: "nosniff" },
  { key: "Referrer-Policy", value: "strict-origin-when-cross-origin" },
  { key: "Permissions-Policy", value: "camera=(), microphone=(), geolocation=()" },
  // Only honored over HTTPS; ignored on plain HTTP, so safe to send everywhere.
  { key: "Strict-Transport-Security", value: "max-age=63072000; includeSubDomains" },
];

const nextConfig: NextConfig = {
  output: "standalone",
  async headers() {
    return [
      {
        source: "/(.*)",
        headers: securityHeaders,
      },
    ];
  },
};

export default nextConfig;
